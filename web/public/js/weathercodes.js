// WMO 天気コード → 絵文字と日本語ラベル
const MAP = [
  [[0], '☀️', '快晴'],
  [[1], '🌤️', '晴れ'],
  [[2], '⛅', '晴れ時々曇り'],
  [[3], '☁️', '曇り'],
  [[45, 48], '🌫️', '霧'],
  [[51, 53, 55], '🌦️', '霧雨'],
  [[56, 57], '🌧️', '着氷性の霧雨'],
  [[61], '🌦️', '小雨'],
  [[63], '🌧️', '雨'],
  [[65], '🌧️', '大雨'],
  [[66, 67], '🌧️', '着氷性の雨'],
  [[71], '🌨️', '小雪'],
  [[73], '🌨️', '雪'],
  [[75, 77], '❄️', '大雪'],
  [[80], '🌦️', 'にわか雨'],
  [[81, 82], '🌧️', '強いにわか雨'],
  [[85, 86], '🌨️', 'にわか雪'],
  [[95], '⛈️', '雷雨'],
  [[96, 99], '⛈️', '雷雨（ひょう）'],
]

export function weatherInfo(code, isDay = 1) {
  if (code == null) return { emoji: '❓', label: '不明' }
  for (const [codes, emoji, label] of MAP) {
    if (codes.includes(code)) {
      if (!isDay && (code === 0 || code === 1)) return { emoji: '🌙', label }
      return { emoji, label }
    }
  }
  return { emoji: '🌡️', label: `天気コード ${code}` }
}
